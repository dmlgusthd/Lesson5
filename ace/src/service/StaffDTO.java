package service;

public class StaffDTO {
	private int st_no;
	private String st_name;
	private String st_sn;
	private String st_graduateday;
	private int st_schoolno;
	private int st_religionno;
	
	public int getSt_no() {
		return st_no;
	}
	public void setSt_no(int st_no) {
		this.st_no = st_no;
	}
	public String getSt_name() {
		return st_name;
	}
	public void setSt_name(String st_name) {
		this.st_name = st_name;
	}
	public String getSt_sn() {
		return st_sn;
	}
	public void setSt_sn(String st_sn) {
		this.st_sn = st_sn;
	}
	public String getSt_graduateday() {
		return st_graduateday;
	}
	public void setSt_graduateday(String st_graduateday) {
		this.st_graduateday = st_graduateday;
	}
	public int getSt_schoolno() {
		return st_schoolno;
	}
	public void setSt_schoolno(int st_schoolno) {
		this.st_schoolno = st_schoolno;
	}
	public int getSt_religionno() {
		return st_religionno;
	}
	public void setSt_religionno(int st_religionno) {
		this.st_religionno = st_religionno;
	}
	
}
